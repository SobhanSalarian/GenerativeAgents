"""
Experimental condition definitions for the MRes validation study.

This module centralises the architecture conditions so the rest of the code
can refer to a single, explicit runtime configuration instead of ad hoc flags.
"""

from dataclasses import dataclass


@dataclass(frozen=True)
class ExperimentalCondition:
  """
  Defines which major cognitive capabilities are enabled for a run.

  Notes
  -----
  These toggles are introduced first as runtime metadata and control
  scaffolding. Behavioural gating can be layered on top incrementally so the
  existing research-scenarios branch stays stable while the experiment
  pipeline evolves.
  """

  name: str
  use_memory: bool
  use_planning: bool
  use_reflection: bool
  description: str
  # When True, the reflection injected each round is generated by an LLM call
  # (genuine Park-style reflection) rather than the deterministic rule-based
  # summary. Defaults to False so all pre-existing conditions and any
  # previously serialised references remain valid and unchanged.
  use_llm_reflection: bool = False

  # P1 experiment: when True, the fixed string in static_reflection_content is
  # injected verbatim as the reflection each round instead of calling any
  # generative or deterministic rule. Used for the injection and placebo arms.
  use_static_reflection: bool = False
  static_reflection_content: str = ""


CONDITIONS = {
  "baseline": ExperimentalCondition(
    name="baseline",
    use_memory=False,
    use_planning=False,
    use_reflection=False,
    description="Minimal agent condition intended to suppress memory, "
                "planning, and reflection.",
  ),
  "memory": ExperimentalCondition(
    name="memory",
    use_memory=True,
    use_planning=False,
    use_reflection=False,
    description="Memory-enabled agents without planning or reflection.",
  ),
  "memory_planning": ExperimentalCondition(
    name="memory_planning",
    use_memory=True,
    use_planning=True,
    use_reflection=False,
    description="Agents with memory and planning enabled but reflection "
                "disabled.",
  ),
  "full": ExperimentalCondition(
    name="full",
    use_memory=True,
    use_planning=True,
    use_reflection=True,
    description="Full condition with memory, planning, and a deterministic "
                "rule-based reflective summary enabled. This is the original "
                "C4 condition used in the May 2026 dataset.",
  ),
  # New condition: identical to 'full' except the reflection injected each
  # round is produced by a genuine LLM synthesis call over the agent's recent
  # episodic memories (Park-style reflection) instead of the deterministic
  # rule. Used to test whether the C3->C4 coordination jump survives when
  # reflection is generative rather than templated.
  "full_llm_reflection": ExperimentalCondition(
    name="full_llm_reflection",
    use_memory=True,
    use_planning=True,
    use_reflection=True,
    use_llm_reflection=True,
    description="Full condition with memory, planning, and LLM-generated "
                "(generative) reflection. Identical to 'full' in every other "
                "respect; the only change is that reflections are synthesised "
                "by an LLM call rather than produced by a deterministic rule.",
  ),

  # P1 — Static reflection injection (confirmatory experiment, not yet run).
  # Memory + planning + the top saturated reflection string from the 'full'
  # condition injected verbatim each step. Tests whether the coordination gain
  # is driven by the reusable informational *content* of reflection rather than
  # by the *process* of generating it.
  # CD injection string: the single most frequent deterministic reflection
  #   (70.2% of all full-condition reflections, 33,027 / 47,040 instances).
  # IC injection string: equivalent top string from the IC full condition
  #   (75.0% of all full-condition reflections).
  # Scenario-specific strings are set in each scenario's _record_scenario_memories;
  # this condition flag activates the injection branch in both scenarios.
  "static_reflection_injection": ExperimentalCondition(
    name="static_reflection_injection",
    use_memory=True,
    use_planning=True,
    use_reflection=True,
    use_llm_reflection=False,
    use_static_reflection=True,
    static_reflection_content=(
        # CD: top saturation string (used by commons_dilemma.py)
        # IC: top saturation string (used by information_consensus.py)
        # Each scenario overrides with its own top string; this field serves
        # as a fallback and is populated per-scenario at injection time.
        "Requesting at or below fair share helped keep the group "
        "within the replenishment limit."
    ),
    description=(
        "P1 injection arm: memory + planning + the top saturated reflection "
        "string from the 'full' condition injected verbatim each step. "
        "Against the placebo, a substantial coordination gain confirms that "
        "the benefit lies in the reusable content, not the generation process."
    ),
  ),

  # P1 — Static reflection placebo.
  # Identical architecture to static_reflection_injection but the injected
  # string has no coordination content. Matched to the injection string on:
  #   - approximate token length (~16 tokens)
  #   - first-person register ("I" / "my")
  #   - present/past tense
  # Contains no reference to resources, fair share, coordination, group
  # behaviour, or voting — so it cannot carry any task-relevant signal.
  "static_reflection_placebo": ExperimentalCondition(
    name="static_reflection_placebo",
    use_memory=True,
    use_planning=True,
    use_reflection=True,
    use_llm_reflection=False,
    use_static_reflection=True,
    static_reflection_content=(
        "Pausing to consider how I approached this decision helps me "
        "stay consistent in my reasoning."
    ),
    description=(
        "P1 placebo arm: memory + planning + a tone-, length-, and "
        "register-matched string with no coordination content injected each "
        "step. If this arm fails to reproduce the coordination gain seen in "
        "static_reflection_injection, the content account of H6 is confirmed."
    ),
  ),
}


DEFAULT_CONDITION_NAME = "full"


def resolve_condition(condition=None):
  """
  Resolve a condition name or ExperimentalCondition object to a canonical
  ExperimentalCondition instance.
  """
  if condition is None:
    return CONDITIONS[DEFAULT_CONDITION_NAME]

  if isinstance(condition, ExperimentalCondition):
    return condition

  if condition not in CONDITIONS:
    valid = ", ".join(sorted(CONDITIONS.keys()))
    raise ValueError(f"Unknown experimental condition '{condition}'. "
                     f"Valid values: {valid}")

  return CONDITIONS[condition]

